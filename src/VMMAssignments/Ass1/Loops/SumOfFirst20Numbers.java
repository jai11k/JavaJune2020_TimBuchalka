package VMMAssignments.Ass1.Loops;

public class SumOfFirst20Numbers
{
    public static void main(String[] args)
    {
        int sum=0;

        for (int i = 1; i <=20; i++)
        {

              sum=sum+i;


        }
        System.out.print("Sum of First 20 Numbers is " +sum )
        ;
    }

}
